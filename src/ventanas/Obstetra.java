   private int obtenerIdObstetra(String username) {
      int id = -1;
      try {
         Connection cn = Conexion.conectar();
         PreparedStatement pst = cn.prepareStatement("SELECT id FROM obstetras WHERE dni = ?");
         pst.setString(1, username); // username es igual al DNI
         ResultSet rs = pst.executeQuery();
         if (rs.next()) {
            id = rs.getInt("id");
         }
         cn.close();
      } catch (Exception e) {
         System.err.println("Error al obtener id_obstetra: " + e);
      }
      return id;
   }

